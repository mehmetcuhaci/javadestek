package Donguler;

public class For {
    public static void main(String[] args) {
        //For ne kadar dönüceğini bildiğimiz noktalarda kullanılır
        int i;
        for (i = 1; i <= 10; i++) {
            System.out.println(i);
        }
        //aynısını while ilede yazabiliriz ama for bu durumlarda daha okunaklı kalabiliyor.

    }
}
