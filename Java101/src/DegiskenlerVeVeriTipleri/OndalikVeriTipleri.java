package DegiskenlerVeVeriTipleri;

public class OndalikVeriTipleri {
    public static void main(String[] args) {
        float vFloat=3.14F;    /* Java'da float ile double ayrımı yapabilmek için değişkene değer verdikten sonra sonuna
        "F" konur  (sadece float için geçerli) */
        double vDouble=3.14;
        System.out.println(vFloat);
        System.out.println(vDouble);
    }
/* Float ile double farkı

 #Float hızlı ve basittir
 #Double yüksek duyarlıklı ve hassas matematiksel işlemlerde kullanılır.
 #Java hassas matematiksel işlemler, örneğin sinüs kosinüs fonksiyonları, double tipi değer döndürür.

 */

}
