package DegiskenlerVeVeriTipleri;

public class TamSayiVeriTipleri {
    public static void main(String[] args) {
        byte vByte=10;
        short vshort=100;
        int vInt=10000;
        long vLong=100000000;

        System.out.println(vByte);
        System.out.println(vshort);
        System.out.println(vInt);
        System.out.println(vLong);
    }
/* Tam Sayı Veri Tipleri Farkları;

 #Byte: 8 bit uzunluğundadır Max 127, Min -128 olabilir,

 #Short: 16 bit uzunluğundadır Max 32,767 Min -32,768 olabilir

 #Integer: En sık kullanılan veri tipidir 32 bit uzunluğundadır.

 #Long: 64 bit uzunluğundadır değer aralığı çok büyüktür.

 Veri Tipleri farklı boyutlarda yer kaplar buna göre seçeriz. Eğer değişkenimizin değer aralığı küçük ise daha az boyut
 kaplaması için Byte veya Short kullanabiliriz.

 */












}
